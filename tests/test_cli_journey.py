class TestLoad:
    def test_load_creates_relative_symlink_and_regenerates_index(self, workspace, run):
        run("init")
        run("new", "integration", "stripe", "Stripe payments API")
        code, out = run("load", "stripe")
        assert code == 0
        assert "Loaded stripe" in out

        link = workspace / "context" / "stripe"
        assert link.is_symlink()
        assert os.readlink(link) == os.path.join("..", "modules-repo", "stripe")

        root_llms = (workspace / "context" / "llms.txt").read_text()
        assert "- [stripe](stripe/llms.txt): Stripe payments API" in root_llms

        system = (workspace / "context" / "system.md").read_text()
        assert "| stripe | integration | Stripe payments API |" in system

    def test_load_warns_on_placeholder_summary(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")  # no summary given
        _, out = run("load", "postgres")
        assert "placeholder summary" in out

    def test_load_multiple(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        code, _ = run("load", "seed", "postgres")
        assert code == 0
        assert (workspace / "context" / "seed").is_symlink()
        assert (workspace / "context" / "postgres").is_symlink()

        root_llms = (workspace / "context" / "llms.txt").read_text()
        assert "[seed](seed/llms.txt)" in root_llms
        assert "[postgres](postgres/llms.txt)" in root_llms

    def test_load_unknown_module_errors(self, workspace, run):
        run("init")
        code, out = run("load", "nope")
        assert code == 1
        assert "not found in modules-repo/" in out

    def test_load_twice_is_noop(self, workspace, run):
        run("init")
        run("load", "seed")
        code, out = run("load", "seed")
        assert code == 0
        assert "Already loaded: seed" in out

    def test_load_generates_env_example_from_secrets(self, workspace, run):
        run("init")
        run("new", "integration", "vault")
        _give_secret(workspace, "vault", "GCTX_TEST_TOKEN")

        run("load", "vault")
        env_example = (workspace / ".env.example").read_text()
        assert "# vault" in env_example
        assert "GCTX_TEST_TOKEN=" in env_example

        run("unload", "vault")
        assert not (workspace / ".env.example").exists()


