"""End-to-end tests for the README quick-start journey.

Each test drives the real CLI entry point (argparse included) inside a
temp directory and asserts the exact on-disk layout the README promises:
init -> new -> load -> env -> validate -> unload.
"""
import os
import shutil

import yaml

from gcontext import __version__

CONTEXT_FILES = [
    "llms.txt",
    "system.md",
    "structure.md",
    "principles.md",
    "module_features.md",
    "secrets.md",
]

GENERATED_HEADER_PREFIX = "<!-- GENERATED by gcontext."

INTEGRATION_INFO_SECTIONS = [
    "## Purpose",
    "## Where it lives",
    "## Auth & access",
    "## Key entities",
    "## Operations",
    "## Examples",
]


def _give_secret(workspace, name, secret):
    """Declare a secret in an existing module's manifest."""
    manifest_path = workspace / "modules-repo" / name / "module.yaml"
    data = yaml.safe_load(manifest_path.read_text())
    data["secrets"] = [secret]
    manifest_path.write_text(yaml.dump(data))


