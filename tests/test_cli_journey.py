class TestValidate:
    def test_validate_fresh_workspace_passes(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        run("new", "task", "migrate-db")
        run("new", "workflow", "release")
        code, out = run("validate")
        assert code == 0
        for name in ["seed", "postgres", "migrate-db", "release"]:
            assert f"{name}  PASS" in out

    def test_validate_fails_on_missing_required_file(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        (workspace / "modules-repo" / "postgres" / "info.md").unlink()
        code, out = run("validate", "postgres")
        assert code == 1
        assert "missing info.md" in out

    def test_validate_fails_on_broken_llms_link(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        llms = workspace / "modules-repo" / "postgres" / "llms.txt"
        llms.write_text(llms.read_text() + "- [gone.md](gone.md): vanished\n")
        code, out = run("validate", "postgres")
        assert code == 1
        assert "links to 'gone.md' which does not exist" in out

    def test_validate_fails_on_name_mismatch(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        manifest = workspace / "modules-repo" / "postgres" / "module.yaml"
        manifest.write_text("name: postgresql\n")
        code, out = run("validate", "postgres")
        assert code == 1
        assert "does not match directory name" in out

    def test_validate_reports_dangling_symlink(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        run("load", "postgres")
        shutil.rmtree(workspace / "modules-repo" / "postgres")
        code, out = run("validate")
        assert code == 1
        assert "dangling symlink" in out

    def test_validate_unknown_module_errors(self, workspace, run):
        run("init")
        code, out = run("validate", "nope")
        assert code == 1
        assert "module directory not found" in out


