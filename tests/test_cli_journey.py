class TestNew:
    def test_new_integration(self, workspace, run):
        run("init")
        code, out = run("new", "integration", "postgres")
        assert code == 0

        mod = workspace / "modules-repo" / "postgres"
        manifest = yaml.safe_load((mod / "module.yaml").read_text())
        assert manifest["name"] == "postgres"
        assert "kind" not in manifest  # integration is the default, omitted

        info = (mod / "info.md").read_text()
        for section in INTEGRATION_INFO_SECTIONS:
            assert section in info

        llms = (mod / "llms.txt").read_text()
        assert "[info.md](info.md)" in llms
        assert "[module.yaml](module.yaml)" in llms
        assert "## Where to write" in llms

        # Created, not loaded
        assert not (workspace / "context" / "postgres").exists()

    def test_new_task(self, workspace, run):
        run("init")
        code, _ = run("new", "task", "migrate-db")
        assert code == 0

        mod = workspace / "modules-repo" / "migrate-db"
        assert yaml.safe_load((mod / "module.yaml").read_text())["kind"] == "task"
        assert "## Goal" in (mod / "brief.md").read_text()
        assert "## Subtasks" in (mod / "status.md").read_text()

    def test_new_workflow(self, workspace, run):
        run("init")
        code, _ = run("new", "workflow", "release")
        assert code == 0

        mod = workspace / "modules-repo" / "release"
        assert yaml.safe_load((mod / "module.yaml").read_text())["kind"] == "workflow"
        assert "## Steps" in (mod / "steps.md").read_text()

    def test_new_with_summary(self, workspace, run):
        run("init")
        run("new", "integration", "stripe", "Stripe payments API")
        llms = (workspace / "modules-repo" / "stripe" / "llms.txt").read_text()
        assert "> Stripe payments API" in llms

    def test_new_rejects_invalid_name(self, workspace, run):
        run("init")
        code, out = run("new", "integration", "bad name!")
        assert code == 1
        assert "Invalid module name" in out
        assert not (workspace / "modules-repo" / "bad name!").exists()

    def test_new_rejects_duplicate(self, workspace, run):
        run("init")
        run("new", "integration", "postgres")
        code, out = run("new", "integration", "postgres")
        assert code == 1
        assert "already exists" in out

    def test_new_requires_workspace(self, workspace, run):
        code, out = run("new", "integration", "postgres")
        assert code == 1
        assert "workspace not initialized" in out


