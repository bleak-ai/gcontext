class TestInit:
    def test_creates_workspace_layout(self, workspace, run):
        code, out = run("init")
        assert code == 0
        assert "Created modules-repo/" in out

        # Seeded starter module, present but NOT loaded
        seed = workspace / "modules-repo" / "seed"
        for fname in ["module.yaml", "llms.txt", "info.md"]:
            assert (seed / fname).is_file()
        assert not (workspace / "context" / "seed").exists()

        # Generated context files
        for fname in CONTEXT_FILES:
            assert (workspace / "context" / fname).is_file()
        assert "No modules loaded." in (workspace / "context" / "system.md").read_text()

        # Bootstrap files
        agents = (workspace / "AGENTS.md").read_text()
        assert "<!-- gcontext:start -->" in agents
        assert "context/system.md" in agents
        assert (workspace / "CLAUDE.md").read_text() == "@AGENTS.md\n"
        assert ".env" in (workspace / ".gitignore").read_text().splitlines()

    def test_context_files_carry_generated_header(self, workspace, run):
        run("init")
        for fname in CONTEXT_FILES:
            content = (workspace / "context" / fname).read_text()
            assert content.startswith(GENERATED_HEADER_PREFIX), fname
            assert content.count(GENERATED_HEADER_PREFIX) == 1, fname

    def test_init_twice_errors_and_overwrites_nothing(self, workspace, run):
        run("init")
        sentinel = workspace / "modules-repo" / "seed" / "my-notes.md"
        sentinel.write_text("precious user content\n")

        code, out = run("init")
        assert code == 1
        assert "already initialized" in out
        assert sentinel.read_text() == "precious user content\n"

    def test_init_recreates_missing_context(self, workspace, run):
        run("init")
        shutil.rmtree(workspace / "context")

        code, out = run("init")
        assert code == 0
        assert "Recreated context/" in out
        for fname in CONTEXT_FILES:
            assert (workspace / "context" / fname).is_file()

    def test_init_refuses_foreign_context_dir(self, workspace, run):
        (workspace / "context").mkdir()
        code, out = run("init")
        assert code == 1
        assert "does not look like a gcontext workspace" in out
        assert not (workspace / "modules-repo").exists()

    def test_init_preserves_existing_user_files(self, workspace, run):
        (workspace / "AGENTS.md").write_text("# My agents\n\nMy own rules.\n")
        (workspace / "CLAUDE.md").write_text("my claude setup\n")
        (workspace / ".gitignore").write_text("node_modules\n")

        run("init")

        agents = (workspace / "AGENTS.md").read_text()
        assert "My own rules." in agents
        assert "<!-- gcontext:start -->" in agents
        assert (workspace / "CLAUDE.md").read_text() == "my claude setup\n"
        gitignore = (workspace / ".gitignore").read_text().splitlines()
        assert "node_modules" in gitignore
        assert ".env" in gitignore


