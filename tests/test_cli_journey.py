class TestQuickStartJourney:
    def test_readme_quick_start(self, workspace, run, monkeypatch):
        monkeypatch.delenv("GCTX_TEST_TOKEN", raising=False)

        # 1. init an empty directory
        code, _ = run("init")
        assert code == 0

        # 2. scaffold an integration
        code, _ = run("new", "integration", "vault", "Secrets vault API")
        assert code == 0
        _give_secret(workspace, "vault", "GCTX_TEST_TOKEN")

        # 3. load it
        code, _ = run("load", "vault")
        assert code == 0
        assert (workspace / "context" / "vault").is_symlink()
        assert "Secrets vault API" in (workspace / "context" / "llms.txt").read_text()
        assert "GCTX_TEST_TOKEN=" in (workspace / ".env.example").read_text()

        # 4. env fails until .env is filled in
        code, _ = run("env")
        assert code == 1
        (workspace / ".env").write_text("GCTX_TEST_TOKEN=s3cret\n")
        code, _ = run("env")
        assert code == 0

        # 5. everything validates
        code, _ = run("validate")
        assert code == 0

        # 6. unload cleans up
        code, _ = run("unload", "vault")
        assert code == 0
        assert not (workspace / "context" / "vault").exists()
        assert not (workspace / ".env.example").exists()
