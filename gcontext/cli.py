_GENERATED_HEADER_PREFIX = "<!-- GENERATED by gcontext."
_GENERATED_HEADER = (
    "<!-- GENERATED by gcontext. This file is rewritten on every "
    "`gcontext load`/`unload`. Do not edit it directly — edit modules in "
    "modules-repo/ instead. -->\n"
)
_PLACEHOLDER_SUMMARY = "One-line summary of this module"


def _require_workspace():
    """Exit with an error unless the CWD contains an initialized workspace."""
    if not MODULES_DIR.is_dir():
        print("Error: workspace not initialized. Run: gcontext init")
        sys.exit(1)
    if not CONTEXT_DIR.is_dir():
        print("Error: context/ is missing. Run: gcontext init to recreate it.")
        sys.exit(1)


def _prepend_header(path: Path) -> None:
    """Prepend the generated-file header to a context/ file (idempotent)."""
    content = path.read_text()
    if not content.startswith(_GENERATED_HEADER_PREFIX):
        path.write_text(_GENERATED_HEADER + "\n" + content)


def _prune_stale_links():
    """Remove context/ symlinks whose modules-repo/ target no longer exists."""
    for p in sorted(CONTEXT_DIR.iterdir()):
        if p.is_symlink() and not p.exists():
            p.unlink()
            print(
                f"Warning: module '{p.name}' is missing from modules-repo/ — "
                "removed stale link from context/."
            )


def _warn_placeholder_summaries(names):
    """Warn about just-loaded modules whose llms.txt summary is the placeholder."""
    from core.llms_gen import extract_module_summary

    for name in names:
        llms_file = MODULES_DIR / name / "llms.txt"
        if llms_file.exists() and extract_module_summary(llms_file.read_text()) == _PLACEHOLDER_SUMMARY:
            print(
                f"Warning: '{name}' still has a placeholder summary in llms.txt — "
                "agents route by these descriptions; fill it in."
            )


def _copy_static_files():
    """Copy static template files into context/."""
    for name in ["principles.md", "module_features.md"]:
        _copy_pkg_file("core", ("templates", name), CONTEXT_DIR / name)
        _prepend_header(CONTEXT_DIR / name)
    _copy_pkg_file("gcontext", ("data", "secrets.md"), CONTEXT_DIR / "secrets.md")
    _prepend_header(CONTEXT_DIR / "secrets.md")


def _regenerate():
    """Regenerate all auto-generated files in context/."""
    _prune_stale_links()
    # OSS-specific system.md template (no cloud-only features like cron jobs
    # or browser apps; mentions the seeded example module).
    template_content = _read_pkg_text("gcontext", "data", "system.md")
    generate_root_llms_txt(CONTEXT_DIR)
    generate_structure_md(CONTEXT_DIR, audience="oss")
    generate_system_md(CONTEXT_DIR, template_content)
    for name in ["llms.txt", "structure.md", "system.md"]:
        _prepend_header(CONTEXT_DIR / name)
    _copy_static_files()
    _generate_env_example()
    _generate_agents_md()
    _generate_claude_md()


def _regen_and_report():
    """Regenerate context/ and print the loaded-module count."""
    _regenerate()
    n = sum(1 for p in CONTEXT_DIR.iterdir() if p.is_symlink())
    print(f"Regenerated context/ ({n} module{'' if n == 1 else 's'})")


# Managed bootstrap block written to AGENTS.md at the project root. AGENTS.md is
# the tool-agnostic convention most coding agents auto-load; it points the agent
# at the generated context/system.md entry file (the CLI cannot inject a system
# prompt the way the hosted platform does, so this pointer is the bootstrap).
_AGENTS_START = "<!-- gcontext:start -->"
_AGENTS_END = "<!-- gcontext:end -->"
_AGENTS_BLOCK = f"""{_AGENTS_START}
## gcontext workspace

This is a gcontext workspace. Before doing anything else, read `context/system.md` — \
it explains how to operate the loaded context modules: navigate the `llms.txt` tree, \
run module scripts, and handle secrets. Re-read it whenever modules are loaded or unloaded.
{_AGENTS_END}"""


def _generate_agents_md():
    """Write/refresh the gcontext bootstrap block in AGENTS.md at the project root.

    Non-destructive: only the content between the gcontext markers is managed;
    any other content the user adds to AGENTS.md is preserved.
    """
    agents = Path.cwd() / "AGENTS.md"
    if not agents.exists():
        agents.write_text(f"# AGENTS.md\n\n{_AGENTS_BLOCK}\n")
        return

    content = agents.read_text()
    pattern = re.compile(
        re.escape(_AGENTS_START) + r".*?" + re.escape(_AGENTS_END), re.DOTALL
    )
    if pattern.search(content):
        new_content = pattern.sub(lambda _: _AGENTS_BLOCK, content)
    else:
        new_content = content.rstrip("\n") + "\n\n" + _AGENTS_BLOCK + "\n"

    if new_content != content:
        agents.write_text(new_content)


def _generate_claude_md():
    """Create CLAUDE.md importing AGENTS.md, if the user doesn't have one.

    Claude Code auto-loads CLAUDE.md (not AGENTS.md); the @import keeps
    AGENTS.md as the single source of truth for every agent. Never touches
    an existing CLAUDE.md.
    """
    claude = Path.cwd() / "CLAUDE.md"
    if not claude.exists():
        claude.write_text("@AGENTS.md\n")


def _generate_env_example():
    """Generate .env.example from all loaded modules' secrets lists."""
    lines = []
    for mod_dir in sorted(CONTEXT_DIR.iterdir()):
        if not mod_dir.is_dir() or mod_dir.name.startswith("."):
            continue
        manifest = read_manifest(mod_dir)
        if manifest.secrets:
            lines.append(f"# {manifest.name}")
            for secret in manifest.secrets:
                lines.append(f"{secret}=")
            lines.append("")
    env_example = Path.cwd() / ".env.example"
    if lines:
        env_example.write_text("\n".join(lines) + "\n")
    elif env_example.exists():
        env_example.unlink()


